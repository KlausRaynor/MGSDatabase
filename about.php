<p>The Metal Gear Solid Database was released with the fourth installment of the 'Solid' series, Metal Gear Solid 4: Sons of the Patriots.  It details the extensive history of the Metal Gear Series dating back to its genesis on the MSX2 in Japan.  Since, it has evolved to an 11 game series that will finally see its conclusion in September 2015.</p>
<p>
This page is dedicated to recreating the Metal Gear Solid Database in its entirety.  The MGS Database was released for the Playstation 3 as a supplemental download to Metal Gear Solid 4, to bring players up to speed on the current story.  This webpage, while still in development, looks to capture the original look and feel of the title, with the content still being fleshed out. </p>
<p> Once fully implemented, the relationship chart builder will allow for custom charts to be created using any custom or fictional characters as well as uploading one's own lore and images to each node.  Defining relationships between characters can be tricky-- and the timeline shows that allies are not always on the same side.  </p>
